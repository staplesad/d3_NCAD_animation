document.addEventListener("DOMContentLoaded", function(event) { 
    const svgWidth = 600;
    const svgHeight = 600;

    const x_orig = 60; 
    const y_orig = 75; 
    
    NUM_EL = 3
    elements = []
    for ( i=0; i< NUM_EL; i++) {
      overlap_x = i*(x_orig/8*7) 
      var ellipse = {'x_axis': svgWidth/2-overlap_x, 'y_axis': svgHeight/2,
                    'colour': 'darkgrey', 'rx': x_orig, 'ry':y_orig};
      var ellipse2 = {'x_axis': svgWidth/2-overlap_x, 'y_axis': svgHeight/2-(y_orig-x_orig),
                    'colour': 'white', 'rx': x_orig, 'ry':y_orig};
      elements.push(ellipse)
      elements.push(ellipse2)
    }


    d3.select('body').append('svg')
      .attr('class', 'svg')
      .attr('width', svgWidth)
      .attr('height', svgHeight);

    d3.select('body').append('button')
      .attr('class', 'button')
      .attr('onclick', 'playMusic(\'stone_drop.wav\')')
      .text('Run Animation');
    
    d3.select('svg').selectAll('ellipse')
        .data(elements)
        .enter()
      .append('ellipse')
        .attr('cx', function(d) {return d.x_axis})
        .attr('cy', function(d) {return d.y_axis})
        .attr('fill', function(d) {return d.colour})
        .attr('rx', function(d) {return d.rx})
        .attr('ry', function(d) {return d.ry})
        .attr('class', function(d){return d.colour})

    var ellipses = d3.selectAll('ellipse')
    y_max = Math.max.apply(null, json_data['pitch'])
    ry_anim  = json_data['pitch'].map(x => x/y_max * svgHeight)

    rx_anim  = json_data['noise'].map(x => x * x_orig)
    colour_anim = json_data['onset']

    soundLen = y_max
    duration = soundLen/json_data['index'].length
    
    r_anim = []
    for (i=0; i< json_data['index'].length; i++) {
      item = [rx_anim[i], ry_anim[i]]
      if (colour_anim[i] == 1){
        item.push('white')
      }
      else {
        item.push('darkgrey')
      }
      r_anim.push(item)
    }
    var grey = d3.selectAll('.darkgrey')
    var white = d3.selectAll('.white')
    var r_anim1 = r_anim
    var r_anim2 = r_anim

    function expand (){
      r_anim1.forEach(function(d, i) {
        grey
          .transition()
          .duration(duration)
          .delay(i * duration)
          .ease(d3.easeCubicInOut)
          .attr("rx", d[0])
          .attr("ry", d[1])
          .attr("fill", d[2])
      });
      r_anim2.forEach(function(d, i) {
        white
          .transition()
          .duration(duration)
          .delay(i * duration)
          .ease(d3.easeCubicInOut)
          .attr("rx", d[0])
          .attr("ry", d[1])
      });
    }

    var button = d3.select('button')
    button.on('click', function(d, i) {expand()})
    console.log(duration)

});
